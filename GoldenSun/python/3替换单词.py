import re

# 字典
字典={
    "罗宾": "“**罗宾**”",
    "杰拉德": "“**杰拉德**”",
    "伊万": "“**伊万**”",
    "米亚莉": "“**米亚莉**”",
    "萨丢罗斯": "“**萨丢罗斯**”",
    "梅娜蒂": "“**梅娜蒂**”",
    "杰斯敏": "“**杰斯敏**”",
    "加西亚": "“**加西亚**”",
    "斯库莱塔": "“**斯库莱塔**”",
    "哈梅特": "“**哈梅特**”",
    "哈莫": "“**哈莫**”",
    "柯兰": "“**柯兰**”",
    "巴比": "“**巴比**”",
    "西芭": "“**西芭**”",
    "阿莱克斯": "“**阿莱克斯**”",
    "贤者之石": "“**贤者之石**”"
}
# 读取文本文件
输入文件路径 = "*.md"
输出文件路径 = "*.md"

with open(输入文件路径, "r", encoding="utf-8") as 输入文件:
    英语文本 = 输入文件.read()

# 使用正则表达式替换英文单词为中文，并统计替换数量
替换计数 = 0
for 英语单词, 中文词语 in 字典.items():
    # 使用 \b 确保完整匹配单词
    替换结果, 替换次数 = re.subn(r'\b' + re.escape(英语单词) + r'\b', 中文词语, 英语文本)
    英语文本 = 替换结果
    替换计数 += 替换次数

# 写回文件
with open(输出文件路径, "w", encoding="utf-8") as 输出文件:
    输出文件.write(英语文本)

print(f"替换完成，共替换 {替换计数} 处，结果已保存到 {输出文件路径} 文件中。")
